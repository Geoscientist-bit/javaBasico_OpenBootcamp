package com.imprimirValores;

public class ImprimirDatos {

    public static void main(String[] args) {

        int numero = 10;
        long numeroLong = 100;
        double numeroDouble = 1528d;
        boolean numeroBoolean = true;
        String datoString = "Hola mundo";
        System.out.println("numeroInt --> " + numero + "\n" + "numero long --> " + numeroLong
                + "\n" + "numeroDouble --> " + numeroDouble + "\n" + "numeroBoolean --> " + numeroBoolean
                + "\n" + "datoString --> " + datoString);
    }
}
